const router = require("express").Router();
const {
  getAllCategory,
  addCategory,
} = require("../controllers/categoriesController");

/**
 * @openapi
 * /category:
 *   get:
 *     summary: Retrieve all categories
 *     tags:
 *       - Category
 *     responses:
 *       200:
 *         description: A list of categories
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 *               items:
 *                 type: object
 *                 properties:
 *                   id:
 *                     type: string
 *                   name:
 *                     type: string
 *                   description:
 *                     type: string
 */
router.get("/category", getAllCategory);

/**
 * @openapi
 * /api/category:
 *   post:
 *     summary: Add a new category
 *     tags:
 *       - Category
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               name:
 *                 type: string
 *               description:
 *                 type: string
 *     responses:
 *       201:
 *         description: Category created successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 id:
 *                   type: string
 *                 name:
 *                   type: string
 *                 description:
 *                   type: string
 *       400:
 *         description: Bad request
 */
router.post("/category", addCategory);

module.exports = router;
